const LOCALES: Record<Locale, Record<string, string>> = {
  'zh-HK': ZH_HK,
  'en': EN,
  'zh-CN': ZH_CN,
  'ja': JA,
  'ko': KO,
};

interface I18nContextValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

/** Substitute {key} placeholders in a translation string. */
function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = params[k];
    return v == null ? `{${k}}` : String(v);
  });
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const { state, dispatch } = useStore();
  const locale: Locale = (state.locale as Locale) || 'zh-HK';

  const setLocale = useCallback((l: Locale) => {
    dispatch({ type: 'SET_LOCALE', payload: l });
  }, [dispatch]);

  const t = useCallback((key: string, params?: Record<string, string | number>) => {
    const raw = LOCALES[locale]?.[key] ?? LOCALES['en'][key] ?? key;
    return interpolate(raw, params);
  }, [locale]);

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    // Fallback for screens not wrapped in provider
    return {
      locale: 'zh-HK' as Locale,
      setLocale: () => {},
      t: (k: string, params?: Record<string, string | number>) => interpolate(LOCALES['en'][k] ?? k, params),
    };
  }
  return ctx;
}
